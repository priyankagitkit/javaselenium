package Assignment;

public class Swapnumber {
    //swap of two number
    public static void main(String[] args) {
        int a=10,b=50;
        System.out.println("Before swap:" +a+" " +b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println("After swap:" +a+" " +b);


    }
}
